export type Confidence = 'عالية' | 'مشهورة' | 'اجتهادية';
export type Tradition = 'تراثي' | 'معاصر';

export interface DreamSource {
  id: string;
  name: string;        // اسم المصدر/الكتاب
  author: string;      // المؤلف
  confidence: Confidence;
  note: string;        // ملاحظة عن درجة التوثيق
}

export interface DreamInterpretation {
  text: string;        // النص أو الخلاصة
  sourceId: string;    // يربط بـ DreamSource.id
  context: string;     // السياق الذي يُرجّح هذا المعنى
  tradition: Tradition;
}

export interface DreamSymbol {
  id: string;
  name: string;            // اسم الرمز (مثل: البحر)
  category: string;        // يربط بـ DreamCategory.id
  icon: string;            // إيموجي
  color: string;           // لون البطاقة
  tags: string[];          // كلمات مرادفة/وسوم للبحث
  summary: string;         // تفسير مختصر محايد
  interpretations: DreamInterpretation[];
  related: string[];       // معرّفات رموز مشابهة
  notes?: string;          // ملاحظات مهمة/تحذير سياقي
}

export interface DreamCategory {
  id: string;
  name: string;
  icon: string;
  color: string;
}

